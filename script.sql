-- =============================================
-- TABLAS
-- =============================================

-- USUARIO
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='USUARIO' AND xtype='U')
CREATE TABLE USUARIO (
    ID        INT IDENTITY(1,1) PRIMARY KEY,
    USUARIO   VARCHAR(50)    NOT NULL UNIQUE,
    PASS      VARCHAR(64)    NOT NULL,
    DIRECCION VARBINARY(256) NULL
)
GO

-- Si la tabla ya existía sin DIRECCION, la agrega
IF NOT EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('USUARIO') AND name='DIRECCION')
    ALTER TABLE USUARIO ADD DIRECCION VARBINARY(256) NULL
GO

-- BITACORA
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='BITACORA' AND xtype='U')
CREATE TABLE BITACORA (
    ID      INT IDENTITY(1,1) PRIMARY KEY,
    USUARIO VARCHAR(50)  NOT NULL,
    ACCION  VARCHAR(100) NOT NULL,
    FECHA   DATETIME     NOT NULL DEFAULT GETDATE()
)
GO

-- MATERIA
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='MATERIA' AND xtype='U')
CREATE TABLE MATERIA (
    ID        INT IDENTITY(1,1) PRIMARY KEY,
    NOMBRE    VARCHAR(100) NOT NULL,
    CODIGO    VARCHAR(20)  NOT NULL UNIQUE,
    MODALIDAD VARCHAR(20)  NOT NULL DEFAULT 'Presencial',
    PESO      INT          NOT NULL DEFAULT 3,
    ACTIVA    BIT          NOT NULL DEFAULT 1
)
GO

-- CORRELATIVA
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='CORRELATIVA' AND xtype='U')
CREATE TABLE CORRELATIVA (
    ID_MATERIA     INT NOT NULL REFERENCES MATERIA(ID),
    ID_CORRELATIVA INT NOT NULL REFERENCES MATERIA(ID),
    PRIMARY KEY (ID_MATERIA, ID_CORRELATIVA)
)
GO

-- ALUMNO_MATERIA (con todas las columnas nuevas de notas)
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='ALUMNO_MATERIA' AND xtype='U')
CREATE TABLE ALUMNO_MATERIA (
    ID                  INT IDENTITY(1,1) PRIMARY KEY,
    ID_USUARIO          INT NOT NULL REFERENCES USUARIO(ID),
    ID_MATERIA          INT NOT NULL REFERENCES MATERIA(ID),
    ESTADO              VARCHAR(30)    NOT NULL DEFAULT 'Cursando',
    NOTA_PARCIAL1       DECIMAL(4,2)   NULL,
    NOTA_PARCIAL2       DECIMAL(4,2)   NULL,
    NOTA_RECUPERATORIO  DECIMAL(4,2)   NULL,
    NOTA_FINAL          DECIMAL(4,2)   NULL,
    FECHA_FINAL         DATETIME       NULL,
    FECHA_RECUPERATORIO DATETIME       NULL,
    DVH                 INT            NULL,
    UNIQUE(ID_USUARIO, ID_MATERIA)
)
GO

-- Si la tabla ya existía con columnas viejas, agregar las nuevas
IF EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('ALUMNO_MATERIA') AND name='NOTA_PARCIAL')
    ALTER TABLE ALUMNO_MATERIA DROP COLUMN NOTA_PARCIAL
GO
IF NOT EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('ALUMNO_MATERIA') AND name='NOTA_PARCIAL1')
    ALTER TABLE ALUMNO_MATERIA ADD NOTA_PARCIAL1 DECIMAL(4,2) NULL
GO
IF NOT EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('ALUMNO_MATERIA') AND name='NOTA_PARCIAL2')
    ALTER TABLE ALUMNO_MATERIA ADD NOTA_PARCIAL2 DECIMAL(4,2) NULL
GO
IF NOT EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('ALUMNO_MATERIA') AND name='NOTA_RECUPERATORIO')
    ALTER TABLE ALUMNO_MATERIA ADD NOTA_RECUPERATORIO DECIMAL(4,2) NULL
GO
IF NOT EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('ALUMNO_MATERIA') AND name='NOTA_FINAL')
    ALTER TABLE ALUMNO_MATERIA ADD NOTA_FINAL DECIMAL(4,2) NULL
GO
IF NOT EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('ALUMNO_MATERIA') AND name='FECHA_FINAL')
    ALTER TABLE ALUMNO_MATERIA ADD FECHA_FINAL DATETIME NULL
GO
IF NOT EXISTS (SELECT 1 FROM sys.columns WHERE object_id=OBJECT_ID('ALUMNO_MATERIA') AND name='FECHA_RECUPERATORIO')
    ALTER TABLE ALUMNO_MATERIA ADD FECHA_RECUPERATORIO DATETIME NULL
GO

-- EVENTO_ACADEMICO
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='EVENTO_ACADEMICO' AND xtype='U')
CREATE TABLE EVENTO_ACADEMICO (
    ID          INT IDENTITY(1,1) PRIMARY KEY,
    ID_MATERIA  INT NOT NULL REFERENCES MATERIA(ID),
    ID_USUARIO  INT NOT NULL REFERENCES USUARIO(ID),
    TIPO        VARCHAR(30)  NOT NULL,
    DESCRIPCION VARCHAR(200) NULL,
    FECHA       DATETIME     NOT NULL,
    PESO        INT          NOT NULL DEFAULT 3
)
GO

-- DVV_NOTAS
IF NOT EXISTS (SELECT * FROM sysobjects WHERE name='DVV_NOTAS' AND xtype='U')
CREATE TABLE DVV_NOTAS (
    ID_USUARIO INT      NOT NULL REFERENCES USUARIO(ID) PRIMARY KEY,
    DVV        INT      NOT NULL,
    FECHA      DATETIME NOT NULL DEFAULT GETDATE()
)
GO

-- =============================================
-- STORED PROCEDURES - USUARIO
-- =============================================

IF OBJECT_ID('USUARIO_LOGIN','P') IS NOT NULL DROP PROCEDURE USUARIO_LOGIN
GO
CREATE PROCEDURE USUARIO_LOGIN
    @usuario VARCHAR(50),
    @pass    VARCHAR(64)
AS
BEGIN
    SELECT ID, USUARIO, DIRECCION
    FROM USUARIO
    WHERE USUARIO = @usuario AND PASS = @pass
END
GO

IF OBJECT_ID('USUARIO_CAMBIAR_PASS','P') IS NOT NULL DROP PROCEDURE USUARIO_CAMBIAR_PASS
GO
CREATE PROCEDURE USUARIO_CAMBIAR_PASS
    @usuario VARCHAR(50),
    @pass    VARCHAR(64)
AS
BEGIN
    UPDATE USUARIO SET PASS = @pass WHERE USUARIO = @usuario
END
GO

IF OBJECT_ID('USUARIO_OBTENER','P') IS NOT NULL DROP PROCEDURE USUARIO_OBTENER
GO
CREATE PROCEDURE USUARIO_OBTENER
    @usuario VARCHAR(50)
AS
BEGIN
    SELECT ID, USUARIO, DIRECCION FROM USUARIO WHERE USUARIO = @usuario
END
GO

IF OBJECT_ID('USUARIO_ACTUALIZAR_DIRECCION','P') IS NOT NULL DROP PROCEDURE USUARIO_ACTUALIZAR_DIRECCION
GO
CREATE PROCEDURE USUARIO_ACTUALIZAR_DIRECCION
    @usuario   VARCHAR(50),
    @direccion VARBINARY(256)
