CREATE PROCEDURE MATERIA_INSERTAR
    @nombre    VARCHAR(100),
    @codigo    VARCHAR(20),
    @modalidad VARCHAR(20),
    @peso      INT
AS
BEGIN
    INSERT INTO MATERIA (NOMBRE, CODIGO, MODALIDAD, PESO) VALUES (@nombre, @codigo, @modalidad, @peso)
    SELECT SCOPE_IDENTITY() AS ID
